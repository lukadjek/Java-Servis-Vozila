    private void btnObrisiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnObrisiActionPerformed

        if (tblVozila.getSelectedRow() != -1) {

            try {
                ModelTabeleVozila mtz = (ModelTabeleVozila) tblVozila.getModel();
                String red = tblVozila.getModel().getValueAt(tblVozila.getSelectedRow(), 1).toString();
                Kontroler.getInstance().obrisiVozilo(tblVozila.getModel().getValueAt(tblVozila.getSelectedRow(), 1).toString());
                mtz.obrisiVozilo(tblVozila.getSelectedRow());

                JOptionPane.showMessageDialog(this, "Obrisano je vozilo koje je imalo ID: " + red, "Brisanje uspesno", JOptionPane.INFORMATION_MESSAGE);

                if (!txtPretrazi.getText().equals("Pretrazi po bilo kom kriterijumu...")) {
                    txtPretrazi.setText("Pretrazi po bilo kom kriterijumu...");
                    txtPretrazi.setForeground(Color.gray);

                } else {

                    lblBrojRezultata.setText("");
                }

            } catch (SQLException ex) {
                Logger.getLogger(_3_SvaVozila.class.getName()).log(Level.SEVERE, null, ex);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Morate izabrati jedno vozilo za brisanje!", "Greska prilikom brisanja", JOptionPane.ERROR_MESSAGE);

            if (!txtPretrazi.getText().equals("Pretrazi po bilo kom kriterijumu...")) {
                txtPretrazi.setText("Pretrazi po bilo kom kriterijumu...");
                txtPretrazi.setForeground(Color.gray);

            } else {

                lblBrojRezultata.setText("");
            }
        }

    }//GEN-LAST:event_btnObrisiActionPerformed

