    private void prikaziSvaVozila() {

        odrediVelicinuRedova();
        odrediVelicinuKolona();

        try {
            // popuni tabelu listom

            List<Vozila> listaZaposlenih = Kontroler.getInstance().prikaziSvaVozila();
            tblVozila.setModel(new ModelTabeleVozila(listaZaposlenih));
        } catch (SQLException ex) {
            Logger.getLogger(_3_SvaVozila.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (tblVozila.getRowCount() != 0 && !txtPretrazi.getText().equals("")) {
            lblBrojVozilaTrenutni.setText("Trenutni broj vozila: " + tblVozila.getRowCount());
        } else {
            lblBrojVozilaTrenutni.setText("Trenutno nema vozila u bazi!");
        }

    }

    private void centrirajSveVrednostiUTabeli() {

        // centriraj nazive kolona
        ((DefaultTableCellRenderer) tblVozila.getTableHeader().getDefaultRenderer())
                .setHorizontalAlignment(JLabel.CENTER);

        // centriraj vrednosti u tabeli
        DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
        centerRenderer.setHorizontalAlignment(JLabel.CENTER);

        for (int kolona = 0; kolona < tblVozila.getColumnCount(); kolona++) {
            tblVozila.getColumnModel().getColumn(kolona).setCellRenderer(centerRenderer);
        }

        // omoguci sortiranje za sve kolone (default sortiranje u koje ne mesam svoje prste)
        tblVozila.setAutoCreateRowSorter(true);
    }

