    private void txtPretraziKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtPretraziKeyReleased
        ModelTabeleVozila table = (ModelTabeleVozila) tblVozila.getModel();
        String pretrazi = txtPretrazi.getText().strip();

        //      String pretrazi2 = txtPretrazi.getText().toUpperCase(); // za registraciju
        TableRowSorter<ModelTabeleVozila> tr = new TableRowSorter<>(table);
        tblVozila.setRowSorter(tr);

        tr.setRowFilter(RowFilter.regexFilter(pretrazi));

        //       tr.setRowFilter(RowFilter.regexFilter(pretrazi2));
        prikaziTrenutniBrojRezultataPretragePreKucanja();
        int brojRedovaTrenutno = tblVozila.getRowCount();

        if (txtPretrazi.getText().length() != 0) {
            lblBrojRezultata.setText("Broj nadjenih rezultata: " + brojRedovaTrenutno);
            txtPretrazi.setForeground(Color.black);
        } else {
            lblBrojRezultata.setText("");
        }

        if (brojRedovaTrenutno == 0) {
            lblBrojRezultata.setText("Nema nadjenih rezultata za dati kriterijum!");
            txtPretrazi.setForeground(Color.red);
        }


    }//GEN-LAST:event_txtPretraziKeyReleased

    private void txtPretraziFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtPretraziFocusLost
        if (txtPretrazi.getText().equals("")) {
            lblBrojRezultata.setText("");
        }

        if (txtPretrazi.getText().isBlank()) {
            txtPretrazi.setText("Pretrazi po bilo kom kriterijumu...");
            txtPretrazi.setForeground(Color.gray);

        } else {
            txtPretrazi.setForeground(Color.black);
        }

    }//GEN-LAST:event_txtPretraziFocusLost

